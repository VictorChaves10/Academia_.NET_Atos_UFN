




int resultado = 0;
Console.Write("Informe a tabuada do número desejado: ");
int tabuada = int.Parse(Console.ReadLine());

for (int i = 0; i <= 10; i++)
{
    resultado = tabuada * i;
    Console.WriteLine($"{tabuada} x {i} = {resultado}");
}



